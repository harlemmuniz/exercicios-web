function nomeExtenso (numero) {
    switch (numero) {
        case 0:
            console.log(`O número ${numero} escrito por extenso é "Zero".`)
        break
        case 1:
            console.log(`O número ${numero} escrito por extenso é "Um".`)
        break
        case 2:
            console.log(`O número ${numero} escrito por extenso é "Dois".`)
        break
        case 3:
            console.log(`O número ${numero} escrito por extenso é "Três".`)
        break
        case 4:
            console.log(`O número ${numero} escrito por extenso é "Quatro".`)
        break
        case 5:
            console.log(`O número ${numero} escrito por extenso é "Cinco".`)
        break
        case 6:
            console.log(`O número ${numero} escrito por extenso é "Seis".`)
        break
        case 7:
            console.log(`O número ${numero} escrito por extenso é "Sete".`)
        break
        case 8:
            console.log(`O número ${numero} escrito por extenso é "Oito".`)
        break
        case 9:
            console.log(`O número ${numero} escrito por extenso é "Nove".`)
        break
        case 10:
            console.log(`O número ${numero} escrito por extenso é "Dez".`)
        default:
            return 'Número fora do intervalo'
    }
}
nomeExtenso(0)
nomeExtenso(1)
nomeExtenso(2)
nomeExtenso(3)
nomeExtenso(4)
nomeExtenso(5)
nomeExtenso(6)
nomeExtenso(7)
nomeExtenso(8)
nomeExtenso(9)
nomeExtenso(10)